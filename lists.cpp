//  List is a collection of similar type of elements. There are
//  two ways of maintaining a list in memory. The first way is
//  to store the elements of the list in an array. However,
//  arrays have some restrictions and disadvantages. The
//  second way of maintaining a list in memory is through a
//  linked list. 


// Single Linked List
//  The beginning of the list is pointed by a pointer named start.
//  This pointer points to the first node of the list. The link part
//  of each node points to the next node in the list, but the link
//  part of the last node has no next node to point to. Thus, it is
//  made NULL. 

// start -> [3,link] -> [5,link] -> [100,nullptr] -> end


